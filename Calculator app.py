class Calculator:
    #add function
    def add (self,a,b):
        sum_result = a+b
        return sum_result

    def subtract (self,a,b):
        subtract_result = a-b
        return subtract_result

    def multiply (self,a,b):
        multiply_result = a*b
        return multiply_result

    def divide (self,a,b):
        divide_result = a/b
        return divide_result